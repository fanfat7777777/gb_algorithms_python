a = input('введите начальную букву ').upper()
b = input('введите конечную букву ').upper()

if 65 <= ord(a) <= 90 and 65 <= ord(b) <= 90:
    print(f'буква "{a}" на {ord(a) - 64} месте в алфавите')
    print(f'буква "{b}" на {ord(b) - 64} месте в алфавите')
    print(f'Между буквами {max(ord(a), ord(b)) - min(ord(a), ord(b)) - 1} букв(ы)')
else:
    print('Введены неверные значения')
